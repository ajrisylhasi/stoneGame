package stonestactoe.models;

/**
 * An Enum representing the cell states inside of the {@code BoardGameModel}.
 */
public enum Cell {
    /**
     * When the stone is transparent.
     */
    EMPTY,
    /**
     * When the stone is red.
     * */
    FIRST,
    /**
     * When the stone is yellow.
     * */
    SECOND,
    /**
     * When the stone is green.
     * */
    THIRD
}
